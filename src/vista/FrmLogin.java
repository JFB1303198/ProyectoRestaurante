    public void validar(){
        String user = txtUsuario.getText();
        String pass = String.valueOf(txtClave.getPassword());
        
        if (!"".equals(user) || !"".equals(pass)) {
            
            u = uDao.usuarioLogin(user, pass);
            if (u.getCorreo()!= null && u.getPass() != null) {                
                Sistema sis = new Sistema(u);
                sis.setVisible(true);
                dispose();
            }else{
                txtError.setText("Error: Usuario o clave incorrecto.");
                txtUsuario.setText("");
                txtClave.setText("");
                //JOptionPane.showMessageDialog(null, "Correo o la Contrase√±a incorrecta");
            }
        }
    }

